CREATE TABLE releases (
    product text,
    version text,
    release_date timestamp,
    link text,
    message text,
    PRIMARY KEY (product, version)
);
CREATE TABLE checkins (
    checkin_id SERIAL PRIMARY KEY,
    timestamp TIMESTAMP,
    product text,
    version text,
    FOREIGN KEY (product, version) REFERENCES releases ON DELETE CASCADE,
    ip text
);
CREATE TABLE params (
    param text,
    value text,
    checkin_id INTEGER REFERENCES checkins ON DELETE CASCADE,
    PRIMARY KEY (checkin_id, param)
);
CREATE INDEX checkins_timestamp
    ON checkins USING btree (timestamp);
CREATE INDEX checkins_ip
    ON checkins USING btree (ip);
